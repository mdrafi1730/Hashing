
public class WithNoGeneric {
	public static void display(Integer[] arr){
		for(Integer i : arr){
			System.out.print(i + "  ");
		}
	}
	public static void display(Double[] arr){
		for(Double i : arr){
			System.out.print(i + "  ");
		}
	}
	public static void display(String[] arr){
		for(String i : arr){
			System.out.print(i + "  ");
		}
	}
	public static void display(Student[] arr){
		for(Student i : arr){
			System.out.print(i + "  ");
		}
	}
}
